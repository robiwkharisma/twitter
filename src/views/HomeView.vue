export default {
  name: 'HomeView',
  components: {
  },
  data() {
    return {
      form: {
        tweet: null,
      }
    }
  },
  computed: {
    ...mapGetters([
      'feeds',
    ])
  },
  methods: {
    async saveTweet() {
      if (this.form.tweet) {
        let params = {
          tweet: this.form.tweet,
          time: new Date(),
          comments: Math.floor(Math.random() * 100),
          retweets: Math.floor(Math.random() * 100),
          loves: Math.floor(Math.random() * 100)
        }
        await this.$store.dispatch("saveTweet", {
          params: params,
        });
      }
      await this.clearTweet()
    },
    clearTweet() {
      this.form.tweet = null
    },
    dateFormat(time) {
      let date = new Date(time)
      const formattedDate = date.toLocaleString("en-GB", {
        day: "numeric",
        month: "short",
        year: "numeric",
        hour: "numeric",
        minute: "2-digit"
      });

      return formattedDate
    }
  },
}
